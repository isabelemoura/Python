# Aspas simples
print('Isabele Alves')
print(1, 'Isabele "Alves"')

# Aspas duplas
print("Isabele Alves")
print(2, "Isabele 'Alves'")

# Escape
print("Isabele \"Alves\"")

# r
print(r"Isabele \"Alves\"")